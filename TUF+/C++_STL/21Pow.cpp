#include <bits/stdc++.h>
using namespace std;

void explainPow()
{
    double base = 2.0;
    double exponent = 5.0;

    // pow(base, exponent) computes base raised to the power exponent
    double result = pow(base, exponent);

    cout << base << " raised to the power " << exponent << " is " << result << endl;
}

int main()
{
    explainPow();
    return 0;
}
